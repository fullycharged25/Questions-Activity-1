#Raj scored 40, 70, 50 and 60 out of 100 in maths, science, Hindi and English. Find the percentage he got?
math = int(input("What was Raj's score in Math? "))
science = int(input("What was Raj's score in Science? "))
hindi = int(input("What was Raj's score in Hindi? "))
english = int(input("What was Raj's score in English? "))
sum_score = math+science+hindi+english
avg_score = sum_score/4
print(avg_score)